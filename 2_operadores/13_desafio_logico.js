// Duas expressões para resultar em TRUE (Aritméticos/Relacionais/Lógicos)

let verdadeira1 = 1 + 1 <= 2 || 3;
let verdadeira2 = 2 - 1 >= 1 !== 3;

console.log(verdadeira1);
console.log(verdadeira2);

// Duas expressões para resultar em False (Aritméticos/Relacionais/Lógicos)

let falsa1 = 8 - 4 > 12 || 15 === "15";
let falsa2 = !(7 !== 7 || "3" !== 3);

console.log(falsa1);
console.log(falsa2);